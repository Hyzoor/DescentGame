package game.enemyfactory;

/**
 * Enumeration representing the different types of enemies in the game.
 */
public enum Enemies {
    /**
     * Represents a ghost enemy.
     */
    GHOST,

    /**
     * Represents a reaper enemy.
     */
    REAPER,

    /**
     * Represents a demon enemy.
     */
    DEMON,

    /**
     * Represents a goblin enemy.
     */
    GOBLIN,

    /**
     * Represents a gollum enemy.
     */
    GOLLUM,

    /**
     * Represents the Sunlight Keeper enemy.
     */
    SUNLIGHTKEEPER
}
